import torch
from transformers import AutoTokenizer, AutoModelForCausalLM, BitsAndBytesConfig

MODEL_ID = "google/gemma-2-9b-it" 

bnb_config = BitsAndBytesConfig(
    load_in_4bit=True,
    bnb_4bit_quant_type="nf4",
    bnb_4bit_compute_dtype=torch.float16,
)

tokenizer = AutoTokenizer.from_pretrained(MODEL_ID)
model = AutoModelForCausalLM.from_pretrained(
    MODEL_ID,
    quantization_config=bnb_config,
    device_map="auto"
)

# ---------------------------------------------------------
# 3. DEFINE GENERATION FUNCTION
# ---------------------------------------------------------
def generate_resume(candidate_details):
    # Construct the User Input part
    user_input_formatted = f"""### Candidate details / Job target:
{candidate_details}

### Additional instructions:
Tone: professional, one-page."""

    # Gemma typically puts "System" instructions inside the User prompt 
    # or uses a specific chat template structure. 
    # We will combine your system instruction + user input.
    messages = [
        {"role": "user", "content": system_instruction + "\n\n" + user_input_formatted}
    ]

    # Apply Chat Template (Crucial for correct special tokens)
    input_ids = tokenizer.apply_chat_template(
        messages, 
        return_tensors="pt", 
        add_generation_prompt=True
    ).to("cuda")

    outputs = model.generate(
        input_ids,
        max_new_tokens=2000,
        temperature=0.7,
        do_sample=True,
        top_p=0.9,
    )

    response = tokenizer.decode(outputs[0][input_ids.shape[1]:], skip_special_tokens=True)
    return response

